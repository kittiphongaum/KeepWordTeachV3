package com.cs.bru.model;

public class Term {
	private int term_id;
	private String termDateStart;
	private String termDateStop;
	private String termDateYear;
	private String termTeachYear;
	private String termTeachTerm;
	
	public int getTerm_id() {
		return term_id;
	}
	public void setTerm_id(int term_id) {
		this.term_id = term_id;
	}
	public String getTermDateStart() {
		return termDateStart;
	}
	public void setTermDateStart(String termDateStart) {
		this.termDateStart = termDateStart;
	}
	public String getTermDateStop() {
		return termDateStop;
	}
	public void setTermDateStop(String termDateStop) {
		this.termDateStop = termDateStop;
	}
	public String getTermDateYear() {
		return termDateYear;
	}
	public void setTermDateYear(String termDateYear) {
		this.termDateYear = termDateYear;
	}
	public String getTermTeachYear() {
		return termTeachYear;
	}
	public void setTermTeachYear(String termTeachYear) {
		this.termTeachYear = termTeachYear;
	}
	public String getTermTeachTerm() {
		return termTeachTerm;
	}
	public void setTermTeachTerm(String termTeachTerm) {
		this.termTeachTerm = termTeachTerm;
	}
	
}
 