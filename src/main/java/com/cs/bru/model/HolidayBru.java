package com.cs.bru.model;

public class HolidayBru {
	private int holidayBruId;
	private String holidayBruDay;
	private String holidayBruMonth;
	private String holidayBruYear;
	private String holidayBruDayofyear;
	public int getHolidayBruId() {
		return holidayBruId;
	}
	public void setHolidayBruId(int holidayBruId) {
		this.holidayBruId = holidayBruId;
	}
	public String getHolidayBruDay() {
		return holidayBruDay;
	}
	public void setHolidayBruDay(String holidayBruDay) {
		this.holidayBruDay = holidayBruDay;
	}
	public String getHolidayBruMonth() {
		return holidayBruMonth;
	}
	public void setHolidayBruMonth(String holidayBruMonth) {
		this.holidayBruMonth = holidayBruMonth;
	}
	public String getHolidayBruYear() {
		return holidayBruYear;
	}
	public void setHolidayBruYear(String holidayBruYear) {
		this.holidayBruYear = holidayBruYear;
	}
	public String getHolidayBruDayofyear() {
		return holidayBruDayofyear;
	}
	public void setHolidayBruDayofyear(String holidayBruDayofyear) {
		this.holidayBruDayofyear = holidayBruDayofyear;
	}
	
}
