package com.cs.bru.model;

public class Calendar {
  private int id;
  private String tarmCalen;
  private String yearCalen;
  private String monthCalen;
  private int dayCalen;
  private String statusDay;
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getTarmCalen() {
	return tarmCalen;
}
public void setTarmCalen(String tarmCalen) {
	this.tarmCalen = tarmCalen;
}
public String getYearCalen() {
	return yearCalen;
}
public void setYearCalen(String yearCalen) {
	this.yearCalen = yearCalen;
}
public String getMonthCalen() {
	return monthCalen;
}
public void setMonthCalen(String monthCalen) {
	this.monthCalen = monthCalen;
}
public int getDayCalen() {
	return dayCalen;
}
public void setDayCalen(int dayCalen) {
	this.dayCalen = dayCalen;
}
public String getStatusDay() {
	return statusDay;
}
public void setStatusDay(String statusDay) {
	this.statusDay = statusDay;
}
}
